fun main(){
    //When Statement
    var itemNumber : Int = 2
    when(itemNumber){
        1 -> print("Clothes")    //Statement 1
        2 -> print("Food")       //Statement 2
        3 -> print("Toiletries") //Statement 3
        else -> println("Invalid Item Number") //Default Statement
    }
}

